// Toy Car Contrustor
function ToyCar(
    brand, 
    model, 
    year, 
    colour, 
    scale, 
    manufacturer, 
    material, 
    vehicleType, 
    tireType, 
    openingParts, 
    detailedExterior,
    collectible,
    price,
    stock,
    sku     
)
{
    this.brand = brand;
    this.model = model;
    this.year = year;
    this.colour = colour;
    this.scale = scale;
    this.manufacturer = manufacturer;
    this.material = material;
    this.vehicleType = vehicleType;
    this.tireType = tireType;
    this.openingParts = openingParts;
    this.detailedExterior = detailedExterior;
    this.collectible = collectible;
    this.price = price;
    this.stock = stock;
    this.sku = sku;
}